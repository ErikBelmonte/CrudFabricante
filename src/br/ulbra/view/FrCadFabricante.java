    public void controlarTela(int op)
    {
        if(op ==1) 
        {
            txtID.setEnabled(false); 
            
            txtMarca.setEnabled(false);
            txtFone.setEnabled(false);
            txtSite.setEnabled(false);
            txtEmail.setEnabled(false);            
            
            btnSalvar.setEnabled(false);
            btnEditar.setEnabled(false);
            btnExcluir.setEnabled(false);
            btnNovo.setEnabled(true);
        }
        else if(op ==2) 
        {
            txtID.setEnabled(false); 
            
            txtMarca.setEnabled(true);
            txtFone.setEnabled(true);
            txtSite.setEnabled(true);
            txtEmail.setEnabled(true);
            
            btnSalvar.setEnabled(true);
            btnEditar.setEnabled(false);
            btnExcluir.setEnabled(false);
            btnNovo.setEnabled(false);
            
            txtMarca.requestFocus();
            limpar();
        }
        else if(op==3) 
        {
            txtID.setEnabled(true); 
            
            txtMarca.setEnabled(true);
            txtFone.setEnabled(true);
            txtSite.setEnabled(true);
            txtEmail.setEnabled(true);
            
            btnSalvar.setEnabled(false);
            btnEditar.setEnabled(true);
            btnExcluir.setEnabled(true);
            btnNovo.setEnabled(true);
        }
    }
    
    public void limpar()
    {
        txtID.setText(null);
        txtMarca.setText(null);
        txtFone.setText(null);
        txtSite.setText(null);
        txtEmail.setText(null);
    }
    
    public void showTable() throws SQLException
    {
        DefaultTableModel modelo = (DefaultTableModel)tbPesquisa.getModel();
        modelo.setNumRows(0);
        FabricanteDao fdao = new FabricanteDao();
        for(Fabricante f: fdao.read())
        {
            modelo.addRow(new Object[]
            {
                f.getId(),                 
                f.getMarca(),
                f.getTelefone(),
                f.getSite(),                
                f.getEmail()
            });
           
        }
    }
    
